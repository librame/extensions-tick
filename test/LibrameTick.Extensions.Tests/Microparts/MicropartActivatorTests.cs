using Xunit;

namespace Librame.Extensions.Microparts
{
    public class MicropartActivatorTests
    {
        [Fact]
        public void Activate()
        {
            var assemblies = MicropartActivator.CreateAssembly(new()).Unwrap();
            Assert.NotEmpty(assemblies);
            
            var httpClient = MicropartActivator.CreateHttpClient(new()).Unwrap();
            Assert.NotNull(httpClient);
        }

    }
}
